// Function to fetch the user's IP address and generate a unique coupon code
async function fetchCouponCode() {
    try {
        // Fetch the user's IP address
        const response = await fetch('https://api.ipify.org?format=json');
        const data = await response.json();
        const ipAddress = data.ip;

        // Get the stored IP and coupon code from localStorage
        const storedIp = localStorage.getItem('ipAddress');
        const storedCouponCode = localStorage.getItem('couponCode');

        // Check if the IP address has changed
        if (storedIp === ipAddress && storedCouponCode) {
            // If IP is the same and coupon code exists, use the stored coupon code
            document.getElementById('couponCode').value = storedCouponCode;
        } else {
            // If IP is different or no coupon code stored, generate a new coupon code
            const newCouponCode = generateCouponCode(ipAddress);

            // Store the new IP address and coupon code in localStorage
            localStorage.setItem('ipAddress', ipAddress);
            localStorage.setItem('couponCode', newCouponCode);

            // Display the new coupon code in the input field
            document.getElementById('couponCode').value = newCouponCode;
        }
    } catch (error) {
        console.error('Error fetching IP address:', error);
        document.getElementById('couponCode').value = 'ERROR';
    }
}

// Function to generate a coupon code based on the IP address
function generateCouponCode(ip) {
    // Create a base36 string from the IP address hash
    const hash = ip.split('.').reduce((acc, octet) => acc + parseInt(octet, 10), 0);
    const base36Hash = hash.toString(36).toUpperCase();

    // Ensure the coupon code is exactly 6 characters long
    let couponCode = base36Hash.padStart(6, '0').slice(-6);

    // Shuffle the characters to add randomness
    couponCode = couponCode.split('').sort(() => Math.random() - 0.5).join('');

    return couponCode;
}

// Function to copy the coupon code to the clipboard
function copyCode() {
    const couponCode = document.getElementById('couponCode');
    couponCode.select();
    couponCode.setSelectionRange(0, 99999); // For mobile devices
    navigator.clipboard.writeText(couponCode.value);
    alert('Coupon code copied: ' + couponCode.value);
}

// Fetch the coupon code when the page loads
window.onload = fetchCouponCode;

