const slidesContainer = document.querySelector('.reviews__slider');
let slides = document.querySelectorAll('.reviews__slide'); // Get all slides after cloning
let currentIndex = 1; // Start at 1 due to cloning of slides
let startX = 0;
let endX = 0;
let autoSlide;

// Clone the first and last slides
const firstClone = slides[0].cloneNode(true);
const lastClone = slides[slides.length - 1].cloneNode(true);

// Append and prepend clones to the slides container
slidesContainer.appendChild(firstClone);
slidesContainer.insertBefore(lastClone, slides[0]);

// Update slides after cloning
slides = document.querySelectorAll('.reviews__slide');

// Set initial position to the first original slide
slidesContainer.style.transform = `translateX(-${currentIndex * 100}%)`;

function showNextSlide() {
    currentIndex++;
    slidesContainer.style.transition = 'transform 0.5s ease-in-out';
    slidesContainer.style.transform = `translateX(-${currentIndex * 100}%)`;

    if (currentIndex >= slides.length - 1) {
        setTimeout(() => {
            slidesContainer.style.transition = 'none';
            currentIndex = 1;
            slidesContainer.style.transform = `translateX(-${currentIndex * 100}%)`;
        }, 500);
    }
}

function showPreviousSlide() {
    currentIndex--;
    slidesContainer.style.transition = 'transform 0.5s ease-in-out';
    slidesContainer.style.transform = `translateX(-${currentIndex * 100}%)`;

    if (currentIndex <= 0) {
        setTimeout(() => {
            slidesContainer.style.transition = 'none';
            currentIndex = slides.length - 2;
            slidesContainer.style.transform = `translateX(-${currentIndex * 100}%)`;
        }, 500);
    }
}

function startAutoSlide() {
    autoSlide = setInterval(showNextSlide, 3000);
}

function stopAutoSlide() {
    if (autoSlide) {
        clearInterval(autoSlide);
    }
}

function handleTouchStart(event) {
    startX = event.touches[0].clientX;
    stopAutoSlide();
}

function handleTouchMove(event) {
    endX = event.touches[0].clientX;
}

function handleTouchEnd() {
    if (startX > endX + 50) {
        showNextSlide();
    } else if (startX < endX - 50) {
        showPreviousSlide();
    }
    startAutoSlide();
}

slidesContainer.addEventListener('touchstart', handleTouchStart);
slidesContainer.addEventListener('touchmove', handleTouchMove);
slidesContainer.addEventListener('touchend', handleTouchEnd);

// Start auto slide only on mobile devices
startAutoSlide();

// Restart auto slide on window resize
window.addEventListener('resize', () => {
    stopAutoSlide();
    startAutoSlide();
});

